namespace RepairRequests
{
    /// <summary>
    /// Логика взаимодействия для CreateRequestWindow.xaml
    /// </summary>
    public partial class CreateRequestWindow : Window
    {
        public CreateRequestWindow()
        {
            InitializeComponent();

            // Инициализация значений ComboBox, если требуется
            EquipmentTypeComboBox.ItemsSource = Enum.GetValues(typeof(EquipmentType));
        }

        private void SaveButton_Click(object sender, RoutedEventArgs e)
        {
            // Create a new request and save it to the repository
            var newRequest = new Request
            {
                RequestId = 0, // New request, ID will be generated by the repository
                DateAdded = DateAddedPicker.SelectedDate ?? DateTime.Now,
                EquipmentType = (EquipmentType)EquipmentTypeComboBox.SelectedItem,
                Model = ModelTextBox.Text,
                ProblemDescription = ProblemDescriptionTextBox.Text,
                ClientName = UserRepository.GetCurrentUser().Login, // Set client name based on logged-in user
                PhoneNumber = PhoneNumberTextBox.Text,
                Status = RequestStatus.НоваяЗаявка
            };

            RequestRepository.AddRequest(newRequest);
            DialogResult = true;
            Close();
        }
    }

}
